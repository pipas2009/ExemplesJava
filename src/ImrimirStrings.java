public class ImrimirStrings {
    public static void main(String[] args) {
        String a = "Hola";
        String b = "Bye";
        String c = "One" + "Two";
        String d = "Line1\nLine2\nLine3";
        String e = "Column1\tColumn2\tColumn3";
        System.out.println(a + "," + b + "," + c + "," + d + "," + e);
    }
}