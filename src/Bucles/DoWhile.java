package Bucles;

public class DoWhile {
    static void main(String[] args) {
        int index=0;
        do{
            System.out.println(index);
            index= index+1;
        } while (index<=100);
        int idex=100;
        do{
            System.out.println(idex);
            index= index-1;
        } while (idex>=0);
    }
}
