package Bucles;

public class While {
    static void main(String[] args) {
        int index=0;
        while (index<=100){
            System.out.println(index);
            index= index+1;
        }
        int idex=100;
        while (idex>=0){
            System.out.println(idex);
            idex= idex-1;
        }
    }
}
